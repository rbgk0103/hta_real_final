package bean;

public class MenuVo {

	int menu_no;
	String menu_type;
	String menu_name;
	int menu_price;
	String menu_image;
	int menu_hit;
	String menu_day;
	String menu_event;
	
	public int getMenu_no() {
		return menu_no;
	}
	public void setMenu_no(int menu_no) {
		this.menu_no = menu_no;
	}
	public String getMenu_type() {
		return menu_type;
	}
	public void setMenu_type(String menu_type) {
		this.menu_type = menu_type;
	}
	public String getMenu_name() {
		return menu_name;
	}
	public void setMenu_name(String menu_name) {
		this.menu_name = menu_name;
	}
	public int getMenu_price() {
		return menu_price;
	}
	public void setMenu_price(int menu_price) {
		this.menu_price = menu_price;
	}
	public String getMenu_image() {
		return menu_image;
	}
	public void setMenu_image(String menu_image) {
		this.menu_image = menu_image;
	}
	public int getMenu_hit() {
		return menu_hit;
	}
	public void setMenu_hit(int menu_hit) {
		this.menu_hit = menu_hit;
	}
	public String getMenu_day() {
		return menu_day;
	}
	public void setMenu_day(String menu_day) {
		this.menu_day = menu_day;
	}
	public String getMenu_event() {
		return menu_event;
	}
	public void setMenu_event(String menu_event) {
		this.menu_event = menu_event;
	}
	
	
	
}
