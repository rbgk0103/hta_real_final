package bean;

public class MenuVo {

	int menuNo;
	String menuType;
	String menuName;
	int menuPrice;
	String menuImage;
	int menuHit;
	String menuDay;
	String menuEvent;
	
	public int getMenuNo() {
		return menuNo;
	}
	public void setMenuNo(int menuNo) {
		this.menuNo = menuNo;
	}
	public String getMenuType() {
		return menuType;
	}
	public void setMenuType(String menuType) {
		this.menuType = menuType;
	}
	public String getMenuName() {
		return menuName;
	}
	public void setMenuName(String menuName) {
		this.menuName = menuName;
	}
	public int getMenuPrice() {
		return menuPrice;
	}
	public void setMenuPrice(int menuPrice) {
		this.menuPrice = menuPrice;
	}
	public String getMenuImage() {
		return menuImage;
	}
	public void setMenuImage(String menuImage) {
		this.menuImage = menuImage;
	}
	public int getMenuHit() {
		return menuHit;
	}
	public void setMenuHit(int menuHit) {
		this.menuHit = menuHit;
	}
	public String getMenuDay() {
		return menuDay;
	}
	public void setMenuDay(String menuDay) {
		this.menuDay = menuDay;
	}
	public String getMenuEvent() {
		return menuEvent;
	}
	public void setMenuEvent(String menuEvent) {
		this.menuEvent = menuEvent;
	}
	
}
